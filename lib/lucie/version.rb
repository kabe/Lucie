module Lucie
  module VERSION #:nodoc:
    unless defined? MAJOR
      MAJOR = 0
      MINOR = 0
      MAINTENANCE = 1
      
      STRING = [MAJOR, MINOR, MAINTENANCE].join('.')
    end
  end
end


